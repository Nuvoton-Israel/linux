typedef struct bit_field {
	u32 offset;
	u32 size;
} bit_field_t;


/*---------------------------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------------------------*/
/*                                  SHA Registers                                                          */
/*---------------------------------------------------------------------------------------------------------*/
/*---------------------------------------------------------------------------------------------------------*/
#define HASH_DATA_IN            (sha_base + 0x000)
#define HASH_CTR_STS            (sha_base + 0x004)
#define HASH_CFG                (sha_base + 0x008)
#define HASH_VER                (sha_base + 0x00C)
#define HASH_DIG_H(i)           (sha_base + 0x020 + (4 * i))

/*---------------------------------------------------------------------------------------------------------*/
/* HASH_CTR_STS fields                                                                                     */
/*---------------------------------------------------------------------------------------------------------*/
